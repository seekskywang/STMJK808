// 热电偶 AD => 温度, 单位为0.001摄氏度
int32_t __tc_meas(const TcTblDesc *pd, const int32_t *tctbl, int32_t ad, int32_t bt) {
//  uint32_t ubx;
  int32_t IDX0_WD = pd->idx0wd;
  int32_t WD_MIN  = pd->wdmin;
  int32_t WD_MAX  = pd->wdmax;
  int32_t low, mid, high,tmp;
 int32_t coldIdx,coldRem,coldAD,btt;
//	   if(Test.ad_bat<18)return;
//  btt=bt;
  if (bt < IDX0_WD * 1000) { bt = IDX0_WD * 1000; } // 约束板温到表范围内
  bt -= IDX0_WD * 1000;
  coldIdx = bt / 1000;                      // 冷端温度索引
  coldRem = bt % 1000;                      // 冷端温度余数
  coldAD  = tctbl[coldIdx];                 // 计算冷端AD值

  coldAD += coldRem * (tctbl[coldIdx + 1] - tctbl[coldIdx]) / 1000;
  ad += coldAD;
   tempOVER=1;
   if (ad <= tctbl[WD_MIN - IDX0_WD]) return WD_MIN * 1000;
  if (ad >= tctbl[WD_MAX - IDX0_WD]) return WD_MAX * 1000;

  low  = WD_MIN - IDX0_WD;
  high = WD_MAX - IDX0_WD;
  while (low <= high) {
    mid = (low + high) >> 1;
    if (tctbl[mid] < ad)
      low = mid + 1;
    else if (tctbl[mid] > ad)
      high = mid - 1;
    else
      break;
  }	  
  if (tctbl[mid] > ad) mid = mid - 1;

  tmp = IDX0_WD * 1000 + mid * 1000;
  btt=tctbl[mid + 1] - tctbl[mid];
//  if(btt!=0)
  tmp += (ad - tctbl[mid]) * 1000 / (tctbl[mid + 1] - tctbl[mid]);
  tempOVER=0;
  return tmp;
}
