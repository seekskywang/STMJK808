extern uint8_t tempOVER;
// PT热电阻 AD => 温度, 单位为0.001摄氏度
int32_t __pt_meas(const PtTblDesc *pd, const int32_t *pttbl, int32_t ad) {
  int32_t PTN   = pd->ptn;
  int32_t PTMIN = pd->wdmin;
  int32_t PTMAX = pd->wdmax;
  int32_t low, mid, high;
	int32_t tmp;
  tempOVER=1;
  if (ad <= pttbl[0]) return PTMIN;
  if (ad >= pttbl[PTN - 1]) return PTMAX;

  low  = 0;
  high = PTN - 1;
  while (low <= high) {
    mid = (low + high) >> 1;
    if (pttbl[mid] < ad)
      low = mid + 1;
    else if (pttbl[mid] > ad)
      high = mid - 1;
    else
      break;
  }
  if (pttbl[mid] > ad) mid = mid - 1;

  tmp = PTMIN + mid * 1000;
  tmp += (ad - pttbl[mid]) * 1000 / (pttbl[mid + 1] - pttbl[mid]);
  tempOVER=0;
  return tmp;
}


